package practice.sorubankasi.loopslar;

public class Soru11 {
    public static void main(String[] args) {
        //11) Bir String’ i tersine çevirmek için kod yazınız. Örnek; Mark ==> kraM
        String str="Mark";
        String sonuc="";
        for (int i =str.length()-1; i>=0 ; i--) {
            sonuc=sonuc+str.charAt(i);
        }
        System.out.println(sonuc);//kraM

























































    }
}
